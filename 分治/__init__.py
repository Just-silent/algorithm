# 分治与递归的关系
# 分治（divide and conquer）的全称称为“分而治之”，分治即是将大问题划分为若干个规模较小、可以直接解决的子问题，然后解决这些子问题，最后将这些子问题的解合并起来，即是原问题的解。
# 分治是一种思想，它不涉及到具体的算法，而大多数情况下，分治都是借由递归来实现的。